    private void cbo_productoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbo_productoItemStateChanged
        //limpia los campos
        cbo_tipo.setSelectedItem("Seleccione");
        opt_mujer.setSelected(true);
        txt_marca.setText("");
        cbo_carga.setSelectedItem("Seleccione");
        txt_pies.setText("");

        //habilita los campos de pantalón
        if (cbo_producto.getSelectedItem().equals("Pantalón")) {
            cbo_tipo.setEnabled(true);
            opt_mujer.setEnabled(true);
            opt_hombre.setEnabled(true);

            txt_marca.setEnabled(false);
            cbo_carga.setEnabled(false);
            txt_pies.setEnabled(false);

            //habilita los campos de la lavadora    
        } else if (cbo_producto.getSelectedItem().equals("Lavadora")) {
            txt_marca.setEnabled(true);
            cbo_carga.setEnabled(true);

            cbo_tipo.setEnabled(false);
            opt_mujer.setEnabled(false);
            opt_hombre.setEnabled(false);
            txt_pies.setEnabled(false);

            //habilita los campos de refrigerador
        } else if (cbo_producto.getSelectedItem().equals("Refrigerador")) {
            txt_marca.setEnabled(true);
            txt_pies.setEnabled(true);

            cbo_tipo.setEnabled(false);
            opt_mujer.setEnabled(false);
            opt_hombre.setEnabled(false);
            cbo_carga.setEnabled(false);

            //deshabilita los campos    
        } else {
            cbo_tipo.setEnabled(false);
            opt_mujer.setEnabled(false);
            opt_hombre.setEnabled(false);
            txt_marca.setEnabled(false);
            cbo_carga.setEnabled(false);
            txt_pies.setEnabled(false);
        }
    }//GEN-LAST:event_cbo_productoItemStateChanged

