    private void btn_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarActionPerformed
        String codigo, tipo, marca, producto;
        int precio, stock, cantidadPies;
        char sexo, carga;

        producto = cbo_producto.getSelectedItem().toString();

        //rescata la información del producto
        codigo = txt_codigo.getText();
        precio = Integer.parseInt(txt_precio.getText());
        stock = Integer.parseInt(txt_stock.getText());

        //rescata la información del pantalón
        if (producto.equals("Pantalón")) {
            tipo = cbo_tipo.getSelectedItem().toString();
            if (opt_mujer.isSelected()) {
                sexo = 'M';
            } else {
                sexo = 'H';
            }

            //crea un pantalón
            Pantalon pantalon = new Pantalon(tipo, sexo, codigo, precio, stock);
            //agrega el pantalón a la colección
            RegistroProducto.agregarProducto(pantalon);
            //rescata la información de la lavadora
        } else if (producto.equals("Lavadora")) {
            marca = txt_marca.getText();
            if (cbo_carga.getSelectedItem().equals("Arriba")) {
                carga = 'A';
            } else {
                carga = 'C';
            }
            //crea una lavadora
            Lavadora lavadora = new Lavadora(carga, marca, codigo, precio, stock);
            //agrega la lavadora a la colección
            RegistroProducto.agregarProducto(lavadora);
            //rescata la información del refrigerador
        } else if (producto.equals("Refrigerador")) {
            marca = txt_marca.getText();
            cantidadPies = Integer.parseInt(txt_pies.getText());
            //crea un refrigerador
            Refrigerador refrigerador = new Refrigerador(cantidadPies, marca, codigo, precio, stock);
            //agrega el refrigerador a la colección
            RegistroProducto.agregarProducto(refrigerador);
        } else {
            System.out.println("Debe elegir un producto");
        }
    }//GEN-LAST:event_btn_ingresarActionPerformed
